# Exercise 10 is it odd or even 
# Declaring a variable along with an input
# Using the percent sign, we can be able to determine if the number is divisible
# Using if else statements and print to give an output to determine if he number is even or odd.
num = int(input("Enter a number: "))  
if (num % 2) == 0:  
   print("{0} is Even number".format(num))  
else:  
   print("{0} is Odd number".format(num))  
   