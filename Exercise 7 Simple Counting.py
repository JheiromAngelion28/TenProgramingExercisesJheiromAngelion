# Exercise 7 Simple Counting
# Using loops to count up or down along with increments and decrements
for number in range (1,51,1):
    print(f"Counting from 1 to 50: in increments of 1.{number}")

for number2 in range (51,-1,-1):
    print(f"Counting down from 50 to 0 in decrements of 1.: {number2}")

for number3 in range (30,51,1):
    print(f"Counting up from 30 to 50 in increments of 1.: {number3}")

for number4 in range (50,8,-2):
    print(f"Counting down from 50 to 10 in decrements of 2.: {number4}")

for number5 in range (100,205,5):
    print(f"Counting up from 100 to 200 in decrements of 5.: {number5}")
